# 查找.cpp or .c or .h or .hpp文件中某字段
#!/bin/bash
echo *-----------------------------------------------------start---line------------------------------------------------------*
echo
find $2 -name "*.[cChH]"  -o -name "*.[cC][pP][pP]" -o -name "*.[hH][pP][pP]" | xargs grep -n $1
echo
echo *------------------------------------------------------end---line-------------------------------------------------------*
