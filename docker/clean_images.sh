# 清除所有临时images
#!/bin/bash

images=`docker images -f "dangling=true" -q`
if [ -z $images ] ; then
echo "\033[31m images not exists\033[0m"
exit 1;
fi

docker rmi $(images) -f
echo "\033[33m *-------------------------------------------------------------------------------------------------------------------------* \033[0m"
docker images
echo "\033[33m *-------------------------------------------------------------------------------------------------------------------------* \033[0m"
